  static List<AvatarItem> get allAvatars => [
    ...avatars,
    ...additionalAdultAvatars,
  ];

  static List<AvatarItem> get adultAvatars => allAvatars
      .where(
        (avatar) =>
            avatar.profileTypes.contains('papa') ||
            avatar.profileTypes.contains('maman'),
      )
      .toList();

  static List<AvatarItem> avatarsForProfileType(String profileType) {
    return allAvatars
        .where((avatar) => avatar.profileTypes.contains(profileType))
        .toList();
  }

  static String assetFromId(String id) {
    return allAvatars.firstWhere((avatar) => avatar.id == id).asset;
  }
